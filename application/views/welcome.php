    <p>BioUno is a project that applies Continuous Integration tools and techniques in Bioinformatics. 
        It uses tools like Jenkins for creating biology workflows and to manage computer 
        clusters. </p>
    <p>Jenkins plug-in API is used for creating wrappers for tools
        like MrBayes, Structure, R and scripts and programs written in many different programming 
        languages, like Perl, PHP, Ruby and Java. And it uses Jenkins slaves 
        as general-purpose computing grid.</p>
