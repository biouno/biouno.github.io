    <h2>Jenkins Plug-ins</h2>
    
    <p>Using Jenkins gives us not only the possibility to use its job 
    scheduling, monitoring and triggering, and notifications. We can 
    also extend Jenkins with plug-ins. There is an update site for 
    BioUno which stores all bio-plugins, like MrBayes and FigTree.</p>
    
    <p>You can install any plug-in automatically using the Web user 
    interface or by running a command in your shell.</p>

